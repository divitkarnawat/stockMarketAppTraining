import React from "react";
import XLSX from "xlsx";
import Button from '@material-ui/core/Button';
const axios = require('axios');

export default class UploadStockPrice extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      data: [] /* Array of Arrays e.g. [["a","b"],[1,2]] */,
      cols: [] /* Array of column objects e.g. { name: "C", K: 2 } */
    };
    this.handleFile = this.handleFile.bind(this);
    
  }
  handleFile(file /*:File*/) {
    /* Boilerplate to set up FileReader */
    const reader = new FileReader();
    const rABS = !!reader.readAsBinaryString;
    reader.onload = e => {
      /* Parse data */
      const bstr = e.target.result;
      const wb = XLSX.read(bstr, { type: rABS ? "binary" : "array", cellText: false, cellDates: true });
      // const wb = XLSX.read(bstr, { type:"binary",  cellDates: true});
      /* Get first worksheet */
      const wsname = wb.SheetNames[0];
      const ws = wb.Sheets[wsname];
      // console.log(rABS, wb);
      /* Convert array of arrays */
      // let ans= [];
      // console.log(wb.SheetNames);
      // console.log(wb.Sheets[wb.SheetNames[0]]);
      // console.log(XLSX.utils.sheet_to_json(wb.Sheets[wb.SheetNames[0]], { header: 0, raw: false}));
      // console.log(XLSX.utils.sheet_to_row_object_array(wb.Sheets[wb.SheetNames[0]]));
        // console.log(ans);
      const data = XLSX.utils.sheet_to_json(ws, { header: 1 , raw: false, dateNF: 'yyyy-mm-dd'});
      let sendDetails = {};
      sendDetails.stockPriceList = data;
      axios.post(`http://localhost:8080/stock_prices/`, sendDetails)
      .then(response => console.log(response))
      .catch(error => console.log(error));
      
      // /* Update state */
      this.setState({ data: data, cols: make_cols(ws["!ref"]) });
    };
    if (rABS) reader.readAsBinaryString(file);
    else reader.readAsArrayBuffer(file);
  }

  render() {
    return (
      <DragDropFile handleFile={this.handleFile}>
        <div className="row">
          <div className="col-xs-12">
            <DataInput handleFile={this.handleFile} />
          </div>
        </div>
       
        <div className="row">
          <div className="col-xs-12">
            <OutTable data={this.state.data} cols={this.state.cols} />
          </div>
        </div>
      </DragDropFile>
    );
  }
}

